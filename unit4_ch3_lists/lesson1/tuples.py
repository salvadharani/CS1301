# Declaring tuples

#Creates myTuple with the tuple (1, 2, 3)
myTuple = (1, 2, 3)

print(myTuple)

myInt1 = 1
myInt2 = 2
myInt3 = 3

#Creates myTuple and assigns it the tuple #(myInt1, myInt2, myInt3)
myTuple = (myInt1, myInt2, myInt3)
print(myTuple)

myString = "Hello, world!"
myFloat = 5.1
myInteger = 5

#Creates myTuple as a tuple with the values
#of myString, myFloat, myInteger
myTuple = (myString, myFloat, myInteger)

print(myTuple)
